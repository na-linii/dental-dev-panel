import { describe, it, expect } from 'vitest'
import { wordsToDigits } from '../wordsToDigits'

describe('wordsToDigits', () => {
  // ── v1 acceptance ─────────────────────────────────────────────────────────

  it('десять часов -> 10 часов', () => {
    expect(wordsToDigits('встреча в десять часов')).toBe('встреча в 10 часов')
  })
  it('пятого мая -> 5 мая', () => {
    expect(wordsToDigits('пятого мая')).toBe('5 мая')
  })
  it('пять тысяч двести рублей -> 5200₽', () => {
    expect(wordsToDigits('пять тысяч двести рублей')).toBe('5200₽')
  })
  it('триста рублей -> 300₽', () => {
    expect(wordsToDigits('триста рублей')).toBe('300₽')
  })
  it('одного пациента -> 1 пациента', () => {
    expect(wordsToDigits('одного пациента ждут')).toBe('1 пациента ждут')
  })

  // ── v2 acceptance ─────────────────────────────────────────────────────────

  it('шестнадцать ноль ноль -> 16:00', () => {
    expect(wordsToDigits('запись на шестнадцать ноль ноль')).toBe('запись на 16:00')
  })
  it('ноль ноль в середине предложения (blocker #1)', () => {
    expect(wordsToDigits('Записала вас на двадцать пятое мая в шестнадцать ноль ноль к доктору Шевелюк'))
      .toBe('Записала вас на 25 мая в 16:00 к доктору Шевелюк')
  })
  it('пятое мая -> 5 мая (nominative)', () => {
    expect(wordsToDigits('пятое мая')).toBe('5 мая')
  })
  it('двадцать пятое мая -> 25 мая', () => {
    expect(wordsToDigits('двадцать пятое мая')).toBe('25 мая')
  })
  it('в десять, в тринадцать и в пятнадцать (comma fix)', () => {
    expect(wordsToDigits('в десять, в тринадцать и в пятнадцать')).toBe('в 10, в 13 и в 15')
  })
  it('сложное предложение с датой и временем', () => {
    expect(
      wordsToDigits('у доктора Титовой есть время двадцать второго мая в десять'),
    ).toContain('22 мая в 10')
  })

  // ── Blocker #2: genitive tens + thousands ─────────────────────────────────

  it('двадцати тысяч -> 20000 (blocker #2)', () => {
    expect(wordsToDigits('Стоимость от двадцати тысяч до пятидесяти тысяч'))
      .toBe('Стоимость от 20000 до 50000')
  })

  // ── Цены ──────────────────────────────────────────────────────────────────

  it('тысяча рублей -> 1000₽', () => {
    expect(wordsToDigits('тысяча рублей')).toBe('1000₽')
  })
  it('пятьсот рублей -> 500₽', () => {
    expect(wordsToDigits('пятьсот рублей')).toBe('500₽')
  })

  // ── Без ложных срабатываний ────────────────────────────────────────────────

  it('обычный текст не меняется', () => {
    expect(wordsToDigits('добрый день уважаемый пациент')).toBe('добрый день уважаемый пациент')
  })
  it('готовые цифры не трогаем', () => {
    expect(wordsToDigits('встреча в 10 часов')).toBe('встреча в 10 часов')
  })
  it('пустая строка', () => {
    expect(wordsToDigits('')).toBe('')
  })
})
